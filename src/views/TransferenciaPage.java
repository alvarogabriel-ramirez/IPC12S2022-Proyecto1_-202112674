    private void btnTransferirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTransferirActionPerformed
         ClientePage clientePage = new ClientePage();
        Cliente[] data = clientePage.listaClientes;
        
        int valorCombo1 = cbOrigen.getSelectedIndex();
        int valorCombo2 = cbDestino.getSelectedIndex();
        String cuiClienteCredito = data[valorCombo1].cuentasAsociadas[valorCombo1].dpiCliente;
        int idCuentaOrigen = data[valorCombo1].cuentasAsociadas[valorCombo1].idCuenta;
        
        String cuiDebito = data[valorCombo2-1].cuentasAsociadas[valorCombo2].dpiCliente;
        int idCuentaDestino = data[valorCombo2-1].cuentasAsociadas[valorCombo2].idCuenta;
        
        double monto = Double.parseDouble(txtMonto.getText());
        
        for (int i = 0; i < data.length; i++) {
            if (data[i] != null) {
                if (data[i].cui == cuiClienteCredito) {
                    for (int j = 0; j < data[i].cuentasAsociadas.length; j++) {
                        if (data[i].cuentasAsociadas[j] != null) {
                            if (data[i].cuentasAsociadas[j].idCuenta == idCuentaOrigen) {
                                data[i].cuentasAsociadas[j].saldo += monto;
                            }
                        }
                    }
                }
            }
        }
        for (int i = 0; i < data.length; i++) {
            if (data[i] != null) {
                if (data[i].cui == cuiDebito) {
                    for (int j = 0; j < data[i].cuentasAsociadas.length; j++) {
                        if (data[i].cuentasAsociadas[j] != null) {
                            if (data[i].cuentasAsociadas[j].idCuenta == idCuentaDestino) {
                                data[i].cuentasAsociadas[j].saldo -= monto;
                                JOptionPane.showMessageDialog(null, "Transferencia Realizada Exitosamente", "InformaciÃ³n", JOptionPane.INFORMATION_MESSAGE);
                            }
                        }
                    }
                }
            }
        }
         System.out.println("--------------------------------");
        for (int i = 0; i < data.length; i++) {
            if (data[i] != null) {
                for (int j = 0; j < data[i].cuentasAsociadas.length; j++) {
                    if (data[i].cuentasAsociadas[j] != null) {
                        data[i].cuentasAsociadas[j].ImprimirCuenta();
                        
                    }
                }
            }
        }
    }//GEN-LAST:event_btnTransferirActionPerformed
 public void ImprimirInfo() {
        ClientePage clientePage = new ClientePage();
        Cliente[] data = clientePage.listaClientes;

        for (int i = 0; i < data.length; i++) {
            if (data[i] != null) {
                for (int j = 0; j < data[i].cuentasAsociadas.length; j++) {
                    if (data[i].cuentasAsociadas[j] != null) {
                        data[i].cuentasAsociadas[j].ImprimirCuenta();
                        cbOrigen.addItem(data[i].cuentasAsociadas[j].idCuenta + " -  Cuenta de " + data[i].nombre);
                        cbDestino.addItem(data[i].cuentasAsociadas[j].idCuenta + " -  Cuenta de " + data[i].nombre);
                    }
                }
            }
        }

    }
