package Clases;

public class Administrador {
    public String userAdminstrador;
    public String password;   

    public Administrador(String userAdminstrador, String password) {
        this.userAdminstrador = userAdminstrador;
        this.password = password;
    }

    public String getUserAdminstrador() {
        return userAdminstrador;
    }

    public void setUserAdminstrador(String userAdminstrador) {
        this.userAdminstrador = userAdminstrador;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

  
  
    
}
